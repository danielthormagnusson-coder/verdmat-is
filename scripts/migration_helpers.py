# 4) column_grant_lockout
# ----------------------------------------------------------------------
def column_grant_lockout(
    conn,
    table: str,
    allowlist_anon: Sequence[str],
    allowlist_auth: Sequence[str] | None = None,
) -> str:
    """REVOKE table-level SELECT and GRANT column-level allowlist per role.

    Mirrors the pattern from apply_column_grant_lockout.py.

    If allowlist_auth is None, authenticated gets the same allowlist as anon.
    Caller wraps in BEGIN/COMMIT (this function executes inside an open txn).

    Returns a short human-readable summary string.
    """
    if allowlist_auth is None:
        allowlist_auth = allowlist_anon
    cols_anon = ",\n  ".join(allowlist_anon)
    cols_auth = ",\n  ".join(allowlist_auth)
    sql = f"""
REVOKE SELECT ON public.{table} FROM anon;
REVOKE SELECT ON public.{table} FROM authenticated;
GRANT SELECT (
  {cols_anon}
) ON public.{table} TO anon;
GRANT SELECT (
  {cols_auth}
) ON public.{table} TO authenticated;
"""
    with conn.cursor() as cur:
        cur.execute(sql)
    return (
        f"public.{table}: anon={len(allowlist_anon)} cols, "
        f"authenticated={len(allowlist_auth)} cols"
    )


