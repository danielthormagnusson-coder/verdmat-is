# Connection helper (small, optional — passes through to psycopg2.connect)
# ----------------------------------------------------------------------
def open_connection(dbconfig_path: Path = DBCONFIG_DEFAULT,
                    autocommit: bool = False):
    """Open a psycopg2 connection from .dbconfig (UTF-8 BOM aware)."""
    if psycopg2 is None:
        raise RuntimeError("psycopg2 not installed")
    url = dbconfig_path.read_text(encoding="utf-8-sig").strip()
    conn = psycopg2.connect(url)
    conn.autocommit = autocommit
    return conn


# ----------------------------------------------------------------------
# 1) apply_migration_sql
# ----------------------------------------------------------------------
def apply_migration_sql(conn, path: Path) -> tuple[bool, int, str]:
    """Read a .sql file and execute it in a transaction.

    Returns (ok, rowcount, message). Caller decides whether to commit
    additional work afterward. On error, the transaction is rolled back
    and (False, 0, error_msg) is returned.

    The migration SQL is expected to contain its own BEGIN/COMMIT blocks
    or be safe to run as a single statement set. psycopg2's behavior:
    if the SQL contains explicit BEGIN/COMMIT, those are respected;
    otherwise the implicit transaction wraps the whole file.
    """
    sql = path.read_text(encoding="utf-8")
    with conn.cursor() as cur:
        try:
            cur.execute(sql)
            rowcount = cur.rowcount if cur.rowcount >= 0 else 0
        except Exception as e:
            conn.rollback()
            return False, 0, f"{type(e).__name__}: {e}"
    return True, rowcount, f"applied {path.name} ({len(sql):,} bytes)"


