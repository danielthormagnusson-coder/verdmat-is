"""migration_helpers.py — reusable patterns for Supabase migrations + Python ops.

Codifies the 7 patterns proven in Phase D1 / D3 / column-grant lockout:

  1. apply_migration_sql        — read .sql file, execute in transaction
  2. generate_rollback_sql      — emit defensive DELETE/RESTORE SQL
  3. unnest_upsert              — bulk INSERT via UNNEST(...) → SELECT, ON CONFLICT
  4. column_grant_lockout       — REVOKE table-level + GRANT column-level per role
  5. subprocess_with_shape_safety — run an external script with row-count guardrails
  6. register_supabase_migration — emit the CLI repair command (agent shell can't run TTY)
  7. set_local_role_and_test    — execute SQL under SET LOCAL ROLE; rollback always

All functions are stateless and safe to import from any script. Connection
objects are passed in; this module does not own its own DB connection.

Usage in a script:

    from migration_helpers import (
        apply_migration_sql, unnest_upsert, column_grant_lockout,
        subprocess_with_shape_safety, set_local_role_and_test,
    )
"""
