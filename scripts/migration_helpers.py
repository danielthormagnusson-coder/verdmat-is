# 2) generate_rollback_sql
# ----------------------------------------------------------------------
def generate_rollback_sql(
    out_path: Path,
    fastnums: Sequence[int],
    tables: Sequence[str],
    comment: str = "",
) -> Path:
    """Emit a defensive multi-table DELETE rollback for a fastnum batch.

    Mirrors the pattern from phase_d3_dryrun.generate_rollback. Wraps
    DELETEs in a single transaction, in REVERSE table order from the
    apply (predictions → sales_history → properties for D3, etc.).
    """
    in_list = ",".join(str(int(fn)) for fn in sorted(set(fastnums)))
    lines = [
        f"-- Rollback SQL — generated by migration_helpers.generate_rollback_sql",
        f"-- Targets {len(fastnums):,} fastnums across {len(tables)} table(s):",
        f"--   {', '.join(tables)}",
    ]
    if comment:
        lines.append(f"-- {comment}")
    lines += [
        "--",
        "-- Wrapped in a single transaction; defensive IN-list so future",
        "-- inserts on the same fastnums are not affected.",
        "",
        "BEGIN;",
    ]
    for t in tables:
        lines.append(f"DELETE FROM public.{t} WHERE fastnum IN ({in_list});")
    lines.append("COMMIT;")
    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return out_path


