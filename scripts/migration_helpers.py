# 3) unnest_upsert
# ----------------------------------------------------------------------
def _na_to_none(series):
    return [None if pd.isna(v) else v for v in series.tolist()]


def unnest_upsert(
    conn,
    table: str,
    df: pd.DataFrame,
    column_types: dict[str, str],
    *,
    on_conflict: str = "DO NOTHING",
    conflict_cols: Sequence[str] = ("fastnum",),
    casts: dict[str, str] | None = None,
    batch_size: int = 500,
) -> int:
    """Bulk INSERT via UNNEST(...) → SELECT, with ON CONFLICT handling.

    Generalizes the phase_d1_apply / phase_d3_apply pattern. Each column
    becomes one psycopg2 named parameter passed as a typed array.

    Parameters
    ----------
    conn: live psycopg2 connection (transaction managed by caller).
    table: fully-qualified or schema-less table name (`public.x` or `x`).
    df: DataFrame whose columns are subset of column_types keys.
    column_types: ordered dict {col_name: postgres_type}.
                  Keys ARE the INSERT column list, in order.
    on_conflict: 'DO NOTHING' or 'DO UPDATE SET ...'.
    conflict_cols: which columns the ON CONFLICT clause keys on.
    casts: optional {col_name: target_type} for in-SELECT casts
           (e.g. {'matseiningar': 'jsonb'} → SELECT matseiningar::jsonb FROM unnest).
    batch_size: rows per UNNEST call (matches phase_d1/d3 default).

    Returns total rows actually INSERTed (sum of rowcounts).
    """
    if psycopg2 is None:
        raise RuntimeError("psycopg2 not installed")
    cols = list(column_types.keys())
    types = list(column_types.values())
    casts = casts or {}

    select_exprs = []
    for c in cols:
        if c in casts:
            select_exprs.append(f"{c}::{casts[c]}")
        else:
            select_exprs.append(c)
    select_clause = ",\n  ".join(select_exprs)
    insert_cols_clause = ", ".join(cols)
    unnest_args = ",\n    ".join(f"%({c})s::{types[i]}[]" for i, c in enumerate(cols))
    unnest_alias = "(" + ", ".join(cols) + ")"
    conflict_clause = (
        f"ON CONFLICT ({', '.join(conflict_cols)}) {on_conflict}"
        if conflict_cols else ""
    )

    sql = f"""
INSERT INTO {table} ({insert_cols_clause})
SELECT
  {select_clause}
FROM unnest(
    {unnest_args}
) AS src{unnest_alias}
{conflict_clause};
"""

    total = 0
    n_batches = (len(df) + batch_size - 1) // batch_size
    for i in range(n_batches):
        sub = df.iloc[i * batch_size:(i + 1) * batch_size]
        params = {c: _na_to_none(sub[c]) for c in cols}
        with conn.cursor() as cur:
            cur.execute(sql, params)
            total += cur.rowcount
    return total


