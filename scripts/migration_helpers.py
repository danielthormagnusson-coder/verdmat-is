# 6) register_supabase_migration
# ----------------------------------------------------------------------
def register_supabase_migration(
    versions: Sequence[str],
    cli_path: str = r"D:\verdmat-is\tools\supabase\supabase.exe",
) -> str:
    """Emit the CLI repair command for the operator to run interactively.

    The agent shell cannot run interactive TTY commands. The 2026-05-21
    + 2026-05-27 Group B entries documented this pattern. This function
    is intentionally a string-emitter, not an executor — it prints the
    exact command Danni runs in a real PowerShell terminal.

    Returns a single command string.
    """
    versions_str = " ".join(versions)
    return f'{cli_path} migration repair --status applied {versions_str}'


