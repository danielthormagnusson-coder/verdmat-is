# 7) set_local_role_and_test
# ----------------------------------------------------------------------
def set_local_role_and_test(
    conn,
    role: str,
    sql: str,
    *,
    expect_error_class: type | None = None,
):
    """Execute SQL inside SET LOCAL ROLE; always ROLLBACK.

    Used for sanity probes (anon view-count match, anon 42501 on excluded
    columns). Mirrors the pattern from apply_column_grant_lockout.py.

    Returns:
      - On success: ('ok', result_rows_or_None, no_error)
      - On expected error class: ('expected_error', None, error_message)
      - On unexpected error: ('unexpected_error', None, error_message)
    """
    if psycopg2 is None:
        raise RuntimeError("psycopg2 not installed")
    with conn.cursor() as cur:
        try:
            cur.execute(f"SET LOCAL ROLE {role}")
            cur.execute(sql)
            try:
                rows = cur.fetchall()
            except Exception:
                rows = None
        except Exception as e:
            conn.rollback()
            cls = type(e).__name__
            if expect_error_class is not None and isinstance(e, expect_error_class):
                return "expected_error", None, f"{cls}: {str(e).strip()[:200]}"
            return "unexpected_error", None, f"{cls}: {str(e).strip()[:200]}"
        finally:
            conn.rollback()  # always unwind SET LOCAL
        return "ok", rows, ""


